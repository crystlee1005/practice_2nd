int main(void)
{
   float radius, area; // 변수 선언  
   
   radius = 10.0; // 상수 - 반지름  선언  
   
   area = 3.141592 * radius * radius; // 상수 - 원의 넓이 선언  
   
   printf("원의 면적 : %f\n", area); // 원의 넓이를 출력  
   
   return 0; 
    
 } 
